def flip(matrix):
    start = 0
    end = len(matrix) - 1
    while start < end:
        matrix[start], matrix[end] = matrix[end], matrix[start]
        end -= 1
        start += 1
    for x in range(len(matrix)):
        for y in range(x):
            matrix[x][y], matrix[y][x] = matrix[y][x], matrix[x][y]
    print(matrix)



